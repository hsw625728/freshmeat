MapModel *static_models[NUM_LEVELS];

@implementation MapGenerator

+ (void)initLevels {
	for (int z = 0; z < NUM_LEVELS; z++) static_models[z] = nil;
}

+ (MapModel*)getMap:(int)level {
	if (static_models[level])
		return static_models[level];
	static_models[level] = [[MapModel alloc] init];
	[static_models[level] initMapData:map_d[level]];
	return static_models[level];
}

+ (int)numLevels {
	return NUM_LEVELS;
}

+ (int)indexOfLevelNamed:(char*)name {
	for (int i = 0; i < NUM_LEVEL_NAMES; i++) {
		if (!strcmp(name, level_names[i]))
			return i;
	}
	return -1;
}

+ (char*)nameOfLevelIndexed:(int)index {
	if (index < 0) return "NOLEVEL";
	return level_names[index];
}


@end
